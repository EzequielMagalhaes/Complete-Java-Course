package application;

public class _28_ModificadoresDeAcesso {
	/*				MODIFICADORES DE ACESSO
	*	° PRIVATE:
			- o membro só pode ser acessado na própria classe.
	*	°(nada):
			- o membro só pode ser acessado nas classes de mesmo pacote.
	*	° PROTECTED:
			- o membro só pode ser acessado no mesmo pacote, bem como em subclasses
			de pacotes diferentes.
	*	° PUBLIC:
			- o membro é acessado por todas as classes (ao menos que ele resida em um módulo
			diferente que não exporte o pacote onde ele está).
	*/
}	